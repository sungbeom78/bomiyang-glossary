#!/usr/bin/env python3
"""
clean_derived_terms.py — words__derived_terms.json 품질 정제
위치: glossary/bin/clean_derived_terms.py

처리 내용:
1. synonym 중 words.id인 것 제거 (별개 단어인데 synonym으로 잘못 분류된 것)
2. synonym 중 words.variants에 이미 있는 것 제거
3. synonym 중 underscore 포함(자연어 아님) 제거
4. synonyms 중 domain 무관 (물리, 범죄 용어 등)은 confidence 기준 필터링
5. variant 중 words.id와 완전 동일한 surface 제거 (자기 자신)
6. 정제된 결과를 원본에 덮어쓰고 리포트 출력
"""
import json
import re
from pathlib import Path

GLOSSARY_DIR = Path(__file__).parent.parent.resolve()
DICT_DIR = GLOSSARY_DIR / "dictionary"
WORDS_PATH  = DICT_DIR / "words.json"
DT_PATH     = DICT_DIR / "words__derived_terms.json"


def load_words() -> tuple[set, set]:
    """words.id 집합, words.variants surface 집합 반환"""
    word_ids = set()
    variant_surfaces = set()
    data = json.loads(WORDS_PATH.read_text(encoding='utf-8'))
    for w in data.get('words', []):
        wid = w.get('id', '').lower().strip()
        word_ids.add(wid)
        for v in w.get('variants', []):
            val = (v.get('value') or v.get('short') or '').lower().strip()
            if val:
                variant_surfaces.add(val)
    return word_ids, variant_surfaces


def is_domain_relevant_synonym(surface: str, canonical_id: str) -> bool:
    """도메인 무관 동의어 필터: 금융/트레이딩 비관련 표현 제거"""
    # underscore 포함 → 자연어 아님
    if '_' in surface or ' ' in surface:
        return False
    # 명백한 비도메인 용어 (분석에서 발견된 것들)
    noise = {
        'culdesac', 'lizard', 'reptilian', 'prison', 'jailbreak',
        'weigh', 'weighbridge', 'equilibrium', 'disinterest', 'evenhandedness',
        'impartiality', 'nonpartisanship', 'dgammadtime', 'gamma_decay',
        'cheap', 'swallow', 'believe', 'accept',
    }
    if surface.lower() in noise:
        return False
    return True


def clean_derived_terms():
    word_ids, variant_surfaces = load_words()
    dt_data = json.loads(DT_PATH.read_text(encoding='utf-8'))
    original_items = dt_data.get('items', [])

    kept = []
    removed = []

    for item in original_items:
        surface    = item.get('surface', '').lower().strip()
        itype      = item.get('type', '')
        canonical  = item.get('canonical_id', '')

        reason = None

        if itype == 'synonym':
            # R1: surface가 words.id와 동일 → synonym이 아니라 별개 단어
            if surface in word_ids:
                reason = f"synonym이지만 words.id={surface!r}와 충돌"
            # R2: surface가 이미 words.variants에 있음 → 중복
            elif surface in variant_surfaces:
                reason = f"synonym이지만 words.variant={surface!r}에 이미 존재"
            # R3: underscore / space 포함 → 자연어 아님
            elif '_' in surface or ' ' in surface:
                reason = f"underscore/공백 포함 표현 (자연어 아님): {surface!r}"
            # R4: 도메인 무관 용어 필터
            elif not is_domain_relevant_synonym(surface, canonical):
                reason = f"도메인 무관 동의어: {surface!r}"

        elif itype == 'variant':
            # R5: surface가 words.id와 동일 (자기 자신)
            if surface == canonical.lower():
                reason = f"variant surface={surface!r}가 canonical_id와 동일 (자기 자신)"

        if reason:
            removed.append({'item': item, 'reason': reason})
        else:
            kept.append(item)

    # 저장
    dt_data['items'] = kept
    dt_data['_note'] = (
        "Auto-generated by migrate_v1_1.py. "
        "Cleaned by clean_derived_terms.py (synonym quality filter, v1.1)."
    )
    DT_PATH.write_text(
        json.dumps(dt_data, ensure_ascii=False, indent=2),
        encoding='utf-8'
    )

    # 리포트
    print(f"=== words__derived_terms.json 정제 결과 ===")
    print(f"  원본 items:  {len(original_items)}")
    print(f"  정제 후:     {len(kept)}")
    print(f"  제거됨:      {len(removed)}")
    print()

    reason_dist: dict[str, int] = {}
    for r in removed:
        key = r['reason'].split(':')[0]
        reason_dist[key] = reason_dist.get(key, 0) + 1
    print("  제거 사유별 분포:")
    for k, v in sorted(reason_dist.items(), key=lambda x: -x[1]):
        print(f"    {k}: {v}건")

    print()
    print("  제거된 항목 샘플 (최대 15건):")
    for r in removed[:15]:
        item = r['item']
        print(f"    [{item.get('type')}] {item.get('surface')!r} -> {item.get('canonical_id')!r}  ({r['reason'].split(':')[0]})")

    return len(original_items), len(kept), len(removed)


if __name__ == "__main__":
    clean_derived_terms()
